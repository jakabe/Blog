function drawLetter3D(letter, fontSize3D){
    //console.log(`drawing ${letter} at size ${fontSize3D}`);
    //strokeWeight(fontSize3D);
    fill(0);
    for( let i =0;i< letter.length; i++){
        //i-th row
        for(let j = 0; j < letter[i].length ; j++){
            //j-th column

            if(letter[i][j] === 1){
                //point(i*fontSize3D, j*fontSize3D);
                rect(i*fontSize3D, -j*fontSize3D,fontSize3D,fontSize3D);
            }
            
        }
    }
    //strokeWeight(1);
}

